import * as React from "react"
import { Slot } from "@radix-ui/react-slot"
import { cva, type VariantProps } from "class-variance-authority"

import { cn } from "@/lib/utils"

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap rounded-md text-sm font-medium transition-smooth focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 [&_svg]:pointer-events-none [&_svg]:size-4 [&_svg]:shrink-0",
  {
    variants: {
      variant: {
        default: "bg-primary text-primary-foreground shadow hover:bg-primary/90",
        destructive:
          "bg-destructive text-destructive-foreground shadow-sm hover:bg-destructive/90",
        outline:
          "border border-input bg-background shadow-sm hover:bg-accent hover:text-accent-foreground",
        secondary:
          "bg-secondary text-secondary-foreground shadow-sm hover:bg-secondary/80",
        ghost: "hover:bg-accent hover:text-accent-foreground",
        link: "text-primary underline-offset-4 hover:underline",
        
        // Kid.Stack AI specific variants
        hero: "gradient-primary text-primary-foreground shadow-glow font-semibold hover:shadow-card hover-scale transition-bounce",
        fun: "gradient-secondary text-secondary-foreground shadow-soft font-semibold hover:shadow-glow hover-bounce transition-bounce",
        success: "gradient-accent text-accent-foreground shadow-soft font-semibold hover:shadow-glow hover-scale",
        learning: "bg-muted text-muted-foreground border border-border hover:gradient-primary hover:text-primary-foreground hover-scale transition-smooth shadow-card",
        pet: "bg-pet-baby text-background border border-pet-teen hover:bg-pet-teen hover-scale transition-bounce shadow-card",
        xp: "bg-xp-gold text-background font-bold hover:bg-xp-diamond hover-scale transition-bounce shadow-glow",
        floating: "gradient-hero text-primary-foreground shadow-glow animate-float hover-scale transition-smooth"
      },
      size: {
        default: "h-9 px-4 py-2",
        sm: "h-8 rounded-md px-3 text-xs",
        lg: "h-10 rounded-md px-8",
        xl: "h-12 rounded-lg px-12 text-base",
        icon: "h-9 w-9",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  }
)

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button"
    return (
      <Comp
        className={cn(buttonVariants({ variant, size, className }))}
        ref={ref}
        {...props}
      />
    )
  }
)
Button.displayName = "Button"

export { Button, buttonVariants }
